# Escreva um programa que retornar o valor hora de um funcionario com base no seu salario mensal e horas trabalhadas por mes

salario_mensal = 10
horas_trabalhadas = 2

valor_hora = salario_mensal / horas_trabalhadas

print(valor_hora)

salario_mensal = input("Qual o seu salario mensal?")
horas_trabalhadas = input("Quantas horas trabalhadas por mes?")

valor_hora = int(salario_mensal) / int(horas_trabalhadas)

print(valor_hora)
